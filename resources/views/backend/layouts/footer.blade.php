        <script>
            document.addEventListener("DOMContentLoaded", function () {

                // 1️⃣ page load par sab submenu collapse
                document.querySelectorAll("#sidebar-menu .sub-menu").forEach(function (el) {
                    el.classList.remove("mm-show");
                });
                document.querySelectorAll("#sidebar-menu li").forEach(function (el) {
                    el.classList.remove("mm-active");
                });

                // 2️⃣ current active page ko identify karo
                let currentUrl = window.location.href.split(/[?#]/)[0];

                document.querySelectorAll("#sidebar-menu a").forEach(function (link) {
                    if (link.href === currentUrl) {

                        // Active item highlight
                        link.classList.add("active");

                        let parentLi = link.closest("li");
                        if (parentLi) parentLi.classList.add("mm-active");

                        // Parent UL open karo (sub-menu)
                        let parentUl = link.closest("ul.sub-menu");
                        if (parentUl) {
                            parentUl.classList.add("mm-show");

                            // Parent LI (main menu)
                            let mainLi = parentUl.closest("li");
                            if (mainLi) {
                                mainLi.classList.add("mm-active");
                            }
                        }
                    }
                });

            });
        </script>

